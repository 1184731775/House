package com.qfedu.pojo;

public class Goodscar {
    private Integer id;

    private Integer goodsCount;

    private Integer goodsDetailColorId;

    private Integer goodsCarStatus;

    private Integer userId;

    public Integer getId() {
        return id;
    }

    public void setId(Integer id) {
        this.id = id;
    }

    public Integer getGoodsCount() {
        return goodsCount;
    }

    public void setGoodsCount(Integer goodsCount) {
        this.goodsCount = goodsCount;
    }

    public Integer getGoodsDetailColorId() {
        return goodsDetailColorId;
    }

    public void setGoodsDetailColorId(Integer goodsDetailColorId) {
        this.goodsDetailColorId = goodsDetailColorId;
    }

    public Integer getGoodsCarStatus() {
        return goodsCarStatus;
    }

    public void setGoodsCarStatus(Integer goodsCarStatus) {
        this.goodsCarStatus = goodsCarStatus;
    }

    public Integer getUserId() {
        return userId;
    }

    public void setUserId(Integer userId) {
        this.userId = userId;
    }
}